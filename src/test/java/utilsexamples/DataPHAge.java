package utilsexamples;

public class DataPHAge {
	
	private int begin;
	private int end;
	private boolean beginBC;
	private boolean endBC;
	private String lifestyle;
	private String material;
	private String[] activity;
	
	public int getBegin() {
		return begin;
	}
	public int getEnd() {
		return end;
	}
	public boolean isBeginBC() {
		return beginBC;
	}
	public boolean isEndBC() {
		return endBC;
	}
	public String getLifestyle() {
		return lifestyle;
	}
	public String getMaterial() {
		return material;
	}
	public String[] getActivity() {
		return activity;
	}
	
	

}
